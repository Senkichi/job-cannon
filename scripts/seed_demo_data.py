_NOW_UTC = datetime.now(UTC).replace(tzinfo=None)


def _ts(days_ago: float, hours_offset: float = 0.0) -> str:
    """Return a naive UTC ISO timestamp N days (and optionally hours) ago."""
    delta = timedelta(days=days_ago, hours=hours_offset)
    return (_NOW_UTC - delta).isoformat(timespec="seconds")


# Sub-score keys must match _SUB_SCORE_KEYS in job_finder/db/_classification.py
_SCORE_KEYS = (
    "title_fit",
    "location_fit",
    "comp_fit",
    "domain_match",
    "seniority_match",
    "skills_match",
)


def _sub_scores(tf: int, lf: int, cf: int, dm: int, sm: int, sk: int) -> dict:
    """Build a sub_scores dict with the canonical key order."""
    return dict(zip(_SCORE_KEYS, [tf, lf, cf, dm, sm, sk], strict=True))


def _rationale(strengths: list[str], gaps: list[str]) -> dict:
    """Build the rationale/fit_analysis dict expected by the templates."""
    return {
        "strengths": strengths,
        "gaps": gaps,
        "talking_points": [
            "Emphasise previous impact metrics when discussing this role.",
            "Highlight cross-functional collaboration experience.",
        ],
        "resume_priority_skills": ["Python", "SQL", "A/B testing", "causal inference"],
    }


def _derive_classification(sub_scores: dict) -> str:
    """Mirror derive_classification logic (no legitimacy_note, no low_signal path)."""
    vals = list(sub_scores.values())
    if any(v == 1 for v in vals):
        return "reject"
    if all(v >= 3 for v in vals):
        return "apply"
    if all(v >= 2 for v in vals):
        return "consider"
    return "skip"


