def _check_guard(target: Path) -> None:
    """Refuse to seed if the target looks like the repo root or a real user dir."""
    # Guard 1: refuse if target is the CWD (likely the repo root).
    try:
        cwd = Path.cwd().resolve()
        resolved = target.resolve()
        if resolved == cwd:
            print(
                f"ERROR: --dir resolves to the current working directory ({resolved}).\n"
                "Refusing to seed: this is likely your repo root.",
                file=sys.stderr,
            )
            sys.exit(1)
    except Exception:
        pass

    # Guard 2: if a jobs.db exists but the sentinel is absent, this is real user data.
    db_file = target / "jobs.db"
    sentinel_file = target / _SENTINEL
    if db_file.exists() and not sentinel_file.exists():
        print(
            f"ERROR: {db_file} exists but {sentinel_file} is absent.\n"
            "Refusing to overwrite: this directory appears to contain real user data.\n"
            "To force a fresh seed in a new directory, delete the directory and re-run.",
            file=sys.stderr,
        )
        sys.exit(1)


def _write_config(target: Path) -> None:
    """Write the minimal demo config.yaml with an absolute db path."""
    config_file = target / "config.yaml"
    # Use forward slashes so YAML is valid on all platforms.
    db_path_str = str(target / "jobs.db").replace("\\", "/")
    # Escape literal {} in YAML values (e.g. weight_overrides: {}) before
    # calling .format() so only {db_path} is substituted.
    safe_template = _DEMO_CONFIG_YAML_TEMPLATE.replace("{}", "{{}}").replace(
        "{db_path}", "{db_path}"
    )
    content = safe_template.format(db_path=db_path_str)
    config_file.write_text(content, encoding="utf-8")


def _seed_db(db_path: str, target: Path) -> None:
    """Run migrations, wipe any prior demo seed, insert fresh demo rows."""
    # Import here so the script can be run from the repo root with uv run.
    from job_finder.web.db_helpers import standalone_connection
    from job_finder.web.db_migrate import run_migrations

    # run_migrations creates the schema up to current version.
    # Pass the target dir as user_data_root so migration 41's backup gate
    # (which checks for a recent backup file) uses the demo dir, not CWD.
    run_migrations(db_path, user_data_root=str(target))

    with standalone_connection(db_path) as conn:
        _wipe_demo_rows(conn)
        _insert_jobs(conn)
        _insert_costs(conn)
        _seed_onboarding_complete(conn)
        conn.commit()


def _wipe_demo_rows(conn: sqlite3.Connection) -> None:
    """Wipe all rows in preparation for a clean re-seed.

    This is a throwaway demo DB (the sentinel guards against real user data),
    so wiping all rows on re-seed is safe and produces the cleanest state.
    """
    conn.execute("DELETE FROM jobs")
    conn.execute("DELETE FROM scoring_costs")
    conn.commit()


